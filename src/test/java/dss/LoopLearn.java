package dss;

public class LoopLearn {

	public static void main(String[] args) {
		
		int[] a= {2,3,5,6,7};
		
		int size=a.length;
		
		System.out.println("The size of array:"  + size);
		
		
		for (int i = 0; i<size; i++) {
			
			System.out.println("The array way");
			
			
		
			
			System.out.println(">>>" + a[i]);
			
		}

	}

}
